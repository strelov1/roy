/** Resolve a catalog icon key (from `~/.roy/connections.yaml`'s `icon:`
 *  field) into one of our inline brand SVGs. Returns the matching
 *  `ProviderName` when known; `null` triggers a neutral fallback in
 *  `ProviderIcon.svelte`. The catalog is sparse today (only `github`); new
 *  yaml entries pick up brand art by adding a row to `PROVIDER_ICON` and a
 *  case here. */
export function catalogIcon(key: string | null | undefined): ProviderName | null {
  switch (key) {
    case 'github':
      return 'github';
    default:
      return null;
  }
}

/** Harness → brand icon. Shared by the chat header and sidebar so a
 *  session's row always carries the same mark as its open view. `SessionInfo.agent`
 *  is wire-typed as `string`, so we accept loose input and return `null` for
 *  anything we don't recognise (callers fall back to a neutral glyph). */
export function agentIcon(agent: string | null | undefined): ProviderName | null {
  switch (agent) {
    case 'claude':
      return 'anthropic';
    case 'codex':
      return 'openai';
    case 'gemini':
      return 'google';
    case 'opencode':
      return 'opencode';
    case 'pi':
      return 'pi';
    default:
      return null;
  }
}

/** Best-effort model id → brand. Used to overlay a "what's actually running"
 *  badge on top of router-style agents (opencode), where the model can come
 *  from any provider. Match on substring so both bare ids (`kimi-k2-thinking`)
 *  and namespaced forms (`kimi-for-coding/kimi-k2-thinking`, `anthropic/claude-…`)
 *  resolve. Returns `null` for unrecognised ids — callers omit the badge. */
export function modelProvider(model: string | null | undefined): ProviderName | null {
  if (!model) return null;
  const m = model.toLowerCase();
  if (m.includes('kimi') || m.includes('moonshot')) return 'moonshot';
  if (m.includes('deepseek')) return 'deepseek';
  if (m.includes('claude') || m.includes('anthropic')) return 'anthropic';
  if (m.includes('gemini') || m.includes('google')) return 'google';
  if (m.includes('gpt') || m.includes('openai')) return 'openai';
  return null;
}
